import java.lang.*;
class statically
{
    public static void main(String args[])
    {
        // for statically typed language we have to declare the data type of variable before using it
        int a=10;
        System.out.println("The value of a is "+a);
        // static language are java, c, c++, c#, etc
        String b = "Hello";
        System.out.println("The Name of b is "+b);
    }
}